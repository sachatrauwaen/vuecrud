<template>
    <el-date-picker
        v-model="model"
        type="datetime"
        format="dd/MM/yyyy HH:mm"
        :picker-options="{firstDayOfWeek: 1}"
        :disabled="disabled"
    ></el-date-picker>
</template>

<script>
//import Vue from 'vue'

export default {
    name: "oa-datetime",

    props: {
        value: {},
        schema: {},
        prop: String,
        options: {}
    },
    computed: {
        model: {
            get() {
                return this.value;
            },
            set(val) {
                this.$emit("input", val);
            }
        },
        disabled(){
             return this.schema["x-ui-disabled"];
        }
    }
};
</script>
